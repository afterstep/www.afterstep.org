The look file is the central configuration point for visual attributes of AfterStep.
For Wharf, WinList and Pager their respective config files may override or complement look settings.<br>
<br>
Files in the directories ~/.afterstep/looks/ and /usr/share/afterstep/looks/ will automatically be shown in Start_menu -> Desktop -> Look and can be selected from there.<br>
