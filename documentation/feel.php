As the name might indicate the Feel file is the central configuration point for
every configuration option dictating AfterStep's behaviour.<br>
<br>
Files in the directories ~/.afterstep/feels/ and /usr/share/afterstep/feels/ will
automatically be shown in Start_menu -> Desktop -> Feel and can be selected from there.<br>
<br>
Placement policy is controled through <? local_doc_url ('visualdoc.php','WindowBox','FeelWindowBox',$srcunset,$subunset); ?>,
which can be defined on a per window base in database.<br>
